type SuggestGroup = { label: string; icon?: string; items: string[] };

// Inline-add 暫存列:有建議自動完成下拉 + ✓ 存 + × 取消
// flat 模式(meds):一條長 list 可滾
// groups 模式(diseases):分區可折疊
function TypeaheadDraft({
  suggestions,
  groups,
  historySet,
  onRemoveHistory,
  onSave,
  onCancel,
  placeholder,
}: {
  suggestions?: string[];
  groups?: SuggestGroup[];
  historySet?: Set<string>;
  onRemoveHistory?: (name: string) => void;
  onSave: (value: string) => void;
  onCancel: () => void;
  placeholder: string;
}) {
  const trans = useT();
  const [query, setQuery] = useState('');
  const [open, setOpen] = useState(true);
  const [expandedGroups, setExpandedGroups] = useState<Set<string>>(new Set());
  const rootRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    if (!open) return;
    const onDown = (e: PointerEvent) => {
      const target = e.target as Element | null;
      if (target && rootRef.current?.contains(target)) return;
      setOpen(false);
    };
    const id = window.setTimeout(
      () => document.addEventListener('pointerdown', onDown, true),
      0,
    );
    return () => {
      window.clearTimeout(id);
      document.removeEventListener('pointerdown', onDown, true);
    };
  }, [open]);

  // flat 過濾(meds)
  const flatFiltered = suggestions
    ? suggestions.filter((s) => fuzzyMatch(query, s))
    : [];
  // 鍵盤選取索引(-1 = 沒選,Enter 就送 query)
  const [selectedIdx, setSelectedIdx] = useState(-1);
  const listRef = useRef<HTMLDivElement>(null);
  // query 變化時重置索引
  useEffect(() => {
    setSelectedIdx(-1);
  }, [query]);
  // 選取項目變化時,捲到可視區
  useEffect(() => {
    if (selectedIdx < 0 || !listRef.current) return;
    const el = listRef.current.querySelector<HTMLElement>(
      `[data-idx="${selectedIdx}"]`,
    );
    el?.scrollIntoView({ block: 'nearest' });
  }, [selectedIdx]);
  // grouped 過濾(diseases) — 打字時自動展開
  const querying = query.trim().length > 0;
  const groupedFiltered = groups
    ? groups
        .map((g) => ({
          ...g,
          items: g.items.filter((s) => fuzzyMatch(query, s)),
        }))
        .filter((g) => g.items.length > 0)
    : [];

  const save = () => {
    const v = query.trim();
    if (v) onSave(v);
    else onCancel();
  };

  return (
    <div ref={rootRef} style={{ position: 'relative', marginBottom: 8 }}>
      <div style={{ display: 'flex', gap: 4 }}>
        <input
          type="text"
          value={query}
          autoFocus
          onChange={(e) => {
            setQuery(e.target.value);
            setOpen(true);
          }}
          onFocus={() => setOpen(true)}
          onKeyDown={(e) => {
            if (e.nativeEvent.isComposing) return;
            if (e.key === 'ArrowDown') {
              e.preventDefault();
              setOpen(true);
              const max = flatFiltered.length - 1;
              setSelectedIdx((i) => Math.min(i + 1, max));
              return;
            }
            if (e.key === 'ArrowUp') {
              e.preventDefault();
              setSelectedIdx((i) => Math.max(i - 1, -1));
              return;
            }
            if (e.key === 'Enter') {
              if (selectedIdx >= 0 && flatFiltered[selectedIdx]) {
                onSave(flatFiltered[selectedIdx]);
              } else {
                save();
              }
              return;
            }
            if (e.key === 'Escape') onCancel();
          }}
          placeholder={placeholder}
          style={{ ...inputStyle, flex: 1 }}
        />
        <button onClick={save} style={confirmBtnStyle} title={trans('common.save')}>
          ✓
        </button>
        <button onClick={onCancel} style={removeBtnStyle} title={trans('common.cancel')}>
          <CrossGlyph size={13} stroke={1.8} />
        </button>
      </div>
      {open &&
        ((suggestions && flatFiltered.length > 0) ||
          (groups && groupedFiltered.length > 0)) && (
          <div
            ref={listRef}
            style={{
              position: 'absolute',
              top: '100%',
              left: 0,
              right: 64,
              background: '#ffffff',
              border: '1px solid #d2d2d7',
              borderRadius: 4,
              boxShadow: '0 4px 12px rgba(0,0,0,0.1)',
              zIndex: 50,
              marginTop: 2,
              maxHeight: 400,
              overflowY: 'auto',
            }}
          >
            {/* C 模式:一條長 list(藥物用) */}
            {suggestions && (
              <>
                <div
                  style={{
                    padding: '4px 10px',
                    fontSize: 10,
                    color: '#86868b',
                    background: '#f5f5f7',
                    borderBottom: '1px solid #e5e4e7',
                  }}
                >
                  共 {flatFiltered.length} 項
                </div>
                {flatFiltered.map((s, i) =>
                  renderItem(
                    s,
                    historySet,
                    onSave,
                    onRemoveHistory,
                    i,
                    i === selectedIdx,
                    trans('common.removeFromHistory'),
                  ),
                )}
              </>
            )}

            {/* D 模式:分組可折疊(疾病用) */}
            {groups &&
              groupedFiltered.map((g) => {
                const expanded = expandedGroups.has(g.label) || querying;
                return (
                  <div key={g.label}>
                    <div
                      onClick={() => {
                        const next = new Set(expandedGroups);
                        if (next.has(g.label)) next.delete(g.label);
                        else next.add(g.label);
                        setExpandedGroups(next);
                      }}
                      style={{
                        display: 'flex',
                        alignItems: 'center',
                        padding: '6px 10px',
                        fontSize: 12,
                        fontWeight: 600,
                        color: '#1d1d1f',
                        background: '#f5f5f7',
                        borderBottom: '1px solid #e5e4e7',
                        cursor: 'pointer',
                        userSelect: 'none',
                        position: 'sticky',
                        top: 0,
                        zIndex: 1,
                      }}
                    >
                      <span style={{ marginRight: 6 }}>
                        {expanded ? '▼' : '▶'}
                      </span>
                      <span style={{ marginRight: 6 }}>{g.icon}</span>
                      <span style={{ flex: 1 }}>{g.label}</span>
                      <span style={{ color: '#86868b', fontWeight: 400 }}>
                        {g.items.length}
                      </span>
                    </div>
                    {expanded &&
                      g.items.map((s) =>
                        renderItem(
                          s,
                          historySet,
                          onSave,
                          onRemoveHistory,
                          undefined,
                          undefined,
                          trans('common.removeFromHistory'),
                        ),
                      )}
                  </div>
                );
              })}
          </div>
        )}
    </div>
  );
}

// 單一項目(共用)
function renderItem(
  s: string,
  historySet: Set<string> | undefined,
  onSave: (v: string) => void,
  onRemoveHistory: ((v: string) => void) | undefined,
  idx?: number,
  highlighted?: boolean,
  removeTitle?: string,
) {
  const isHistory = historySet?.has(s);
  return (
    <div
      key={s}
      data-idx={idx}
      style={{
        display: 'flex',
        alignItems: 'center',
        padding: '6px 10px',
        fontSize: 13,
        cursor: 'pointer',
        borderBottom: '1px solid #f2f2f7',
        background: highlighted ? '#e6f0ff' : '#ffffff',
      }}
      onMouseEnter={(e) => (e.currentTarget.style.background = '#f0f7ff')}
      onMouseLeave={(e) =>
        (e.currentTarget.style.background = highlighted ? '#e6f0ff' : '#ffffff')
      }
    >
      {isHistory && (
        <span style={{ marginRight: 6, fontSize: 11, color: '#86868b' }}>
          🕐
        </span>
      )}
      <span
        style={{ flex: 1 }}
        onMouseDown={(e) => {
          e.preventDefault();
          onSave(s);
        }}
      >
        {s}
      </span>
      {isHistory && onRemoveHistory && (
        <button
          onMouseDown={(e) => {
            e.preventDefault();
            e.stopPropagation();
            onRemoveHistory(s);
          }}
          style={{
            width: 20,
            height: 20,
            padding: 0,
            background: 'transparent',
            border: 'none',
            borderRadius: 3,
            cursor: 'pointer',
            color: '#ff3b30',
            fontSize: 14,
            lineHeight: 1,
          }}
          title={removeTitle ?? '從歷史移除'}
        >
          ×
        </button>
      )}
    </div>
  );
}

